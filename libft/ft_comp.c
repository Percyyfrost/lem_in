#include "libft.h"

int		comp(char **str1)
{
	int i;
	int j;

	j = 0;
	i = 0;
	char *signs[4] =  {
		">",
		"<",
		">>",
		"<<"
	};
	while(str1[i])
	{
		while(j < 4)
		{
			if((ft_strcmp(str1[i], signs[j])) == 0)
				return(1);
			j++;
		}
		j = 0;
		i++;
	}
	return(0);
}
